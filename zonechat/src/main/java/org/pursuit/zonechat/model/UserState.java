package org.pursuit.zonechat.model;

public final class UserState {
    private String group;
    private String zone;

    public UserState() {
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }
}
